// READY_STATE_LOADING	        1  => estado de cargando
// READY_STATE_LOADED	        2  => estado de cargado 
// READY_STATE_INTERACTIVE	    3  => estado enteractivo
// READY_STATE_COMPLETE	        4  => terminado el processo de petticion


